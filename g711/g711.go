const (
	uLawClip = 0x7F7B
	uLawBias = 0x84
)

// ULawEncode encodes a 16bit LPCM sample to u-law PCM.
func ULawEncode(sample int16) uint8 {
	sign := (sample >> 8) & 0x80
	if sign != 0 {
		sample = -sample
	}
	if sample > uLawClip {
		sample = uLawClip
	}

	sample = sample + uLawBias
	exponent := muLawExponentTable[(sample>>7)&0xFF]
	mantissa := (sample >> (exponent + 3)) & 0x0F
	return uint8(^(sign | (exponent << 4) | mantissa))
}
